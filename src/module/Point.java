package module;

/**
 *
 * @author Chong
 */
public class Point {
    private float x;
    private float y;
    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }
    
    public float getX() {
        return x;
    }
    
    public float getY() {
        return y;
    }
}
    